<h1 style="color:cyan"><ins>History</ins></h1>
<p></p><pre>DREK SCHOOLS is multilevel educational institute located in Nairobi,Kenya P.O BOX 22342-00200.
The institute was established in 1940 by the then President of the Republic of Kenya the late 
H.E Daniel Arap Moi.The institute started as a community primary school but has grown over the years to 
include Kindergaten,secondary and University levels.</pre>
<p></p><pre>The school started with four tutors and two non-teaching
staff.Currently it has more than 70 certified and qualified tutors and over 20 support staff.The tutor
to student ratio across all the levels is 1:30.This makes sure that your child is well attended to and 
no student feels unattended to.</pre><p></p>
